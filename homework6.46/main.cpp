#include <iostream>
#include <cmath>

using namespace std;

int main()
{
    cout << " Ceil: 1.345 = " << ceil(1.345) << endl;
    cout << "   Cos: 3.14 = " << cos(3.14) << endl;
    cout << "    exp: 3.0 = " << exp(3.0) << endl;
    cout << "  Fabs: -2.3 = " << fabs(-2.3) << endl;
    cout << " Floor: 10.6 = " << floor(10.6) << endl;
    cout << "Fmod:7.2/4.2 = " << fmod(7.2,4.2) << endl;
    cout << "Log:2.718282 = " << log(2.718282) << endl;
    cout << "Log10: 100.0 = " << log10(100.0) << endl;
    cout << "   Pow: 5.,4 = " << pow(5,4) << endl;
    cout << "   Sin: 3.14 = " << sin(3.14) << endl;
    cout << " Sqrt: 100.0 = " << sqrt(100.0) << endl;
    cout << "    Tan: 3.0 = " << tan(3.0) << endl;
}
